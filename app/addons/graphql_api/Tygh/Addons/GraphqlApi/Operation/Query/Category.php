namespace Tygh\Addons\GraphqlApi\Operation\Query;

use Tygh\Addons\GraphqlApi\Context;
use Tygh\Addons\GraphqlApi\Operation\OperationInterface;

class Category implements OperationInterface
{
    /**
     * @var mixed $source
     */
    protected $source;

    /**
     * @var array
     */
    protected $args;

    /**
     * @var \Tygh\Addons\GraphqlApi\Context
     */
    protected $context;

    public function __construct($source, array $args, Context $context)
    {
        $this->source = $source;
        $this->args = $args;
        $this->context = $context;
    }

    public function run()
    {
        $category_id = (string) $this->args['id'];

        list($categories,) = fn_get_categories([
            'item_ids'           => $category_id,
            'category_delimiter' => $this->args['category_delimiter'],
            'simple'             => false,
            'plain'              => true,
            'group_by_level'     => true,
        ], $this->context->getLanguageCode());

        $category = reset($categories)
            ?: [];

        return $category;
    }

    public function getPrivilege()
    {
        return 'view_catalog';
    }

    public function getCustomerPrivilege()
    {
        return false;
    }
}

namespace Boxberry\Models;


/**
 * Class ShortPoint
 * @package Boxberry\Models
 */
class ShortPoint extends AbstractModel
{
    /**
     * @var string
     */
    protected $code = null;
    /**
     * @var string
     */
    protected $name = null;
    /**
     * @var string
     */
    protected $city = null;

    /**
     * @return string
     */
    public function getCode()
    {
        return $this->code;
    }

    /**
     * @param string $code
     */
    public function setCode($code)
    {
        $this->code = $code;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getCity()
    {
        return $this->city;
    }

    /**
     * @param string $city
     */
    public function setCity($city)
    {
        $this->city = $city;
    }

}